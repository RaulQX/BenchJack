    private String getPathToCardImage() {
        String digits = bench.getLast10Digits();
        StringBuilder tmp = new StringBuilder("Images/Cards/");
        int c = Integer.parseInt(String.valueOf(digits.charAt(countCardsDraw)));
        if(c == 1)
            tmp.append("10");
        else if(c == 9) {
            int randomNum = ThreadLocalRandom.current().nextInt(1, 5);
            switch (randomNum) {
                case 2 -> tmp.append("J");
                case 3 -> tmp.append("Q");
                case 4 -> tmp.append("K");
                default -> tmp.append("9");
            }
        }
        else
            tmp.append(c);

        int randomNum = ThreadLocalRandom.current().nextInt(1, 5);
        switch (randomNum) {
            case 1 -> tmp.append("D");
            case 2 -> tmp.append("H");
            case 3 -> tmp.append("S");
            default -> tmp.append("C");
        }

        return tmp.append(".png").toString();
    }

